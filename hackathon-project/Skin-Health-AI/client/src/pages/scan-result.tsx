import { useRoute } from "wouter";
import { Layout } from "@/components/layout";
import { useScan } from "@/hooks/use-scans";
import { AlertTriangle, Info, CheckCircle2, ChevronLeft, Calendar } from "lucide-react";
import { motion } from "framer-motion";
import { Link } from "wouter";

export default function ScanResult() {
  const [, params] = useRoute("/scan/:id");
  const scanId = params?.id ? parseInt(params.id, 10) : null;
  
  const { data: scan, isLoading, isError } = useScan(scanId);

  if (isLoading) {
    return (
      <Layout>
        <div className="animate-pulse space-y-8">
          <div className="h-8 bg-muted rounded w-48"></div>
          <div className="grid grid-cols-1 lg:grid-cols-5 gap-8">
            <div className="lg:col-span-2 h-[400px] bg-muted rounded-3xl"></div>
            <div className="lg:col-span-3 space-y-6">
              <div className="h-40 bg-muted rounded-2xl"></div>
              <div className="h-32 bg-muted rounded-2xl"></div>
              <div className="h-48 bg-muted rounded-2xl"></div>
            </div>
          </div>
        </div>
      </Layout>
    );
  }

  if (isError || !scan) {
    return (
      <Layout>
        <div className="text-center py-20">
          <AlertTriangle className="w-16 h-16 text-destructive mx-auto mb-4" />
          <h2 className="text-2xl font-bold">Scan not found</h2>
          <p className="text-muted-foreground mt-2 mb-6">We couldn't retrieve the details for this scan.</p>
          <Link href="/">
            <a className="inline-flex items-center justify-center px-6 py-3 rounded-xl bg-primary text-white font-medium hover:bg-primary/90 transition-colors">
              Start New Scan
            </a>
          </Link>
        </div>
      </Layout>
    );
  }

  const formattedDate = new Date(scan.createdAt).toLocaleDateString('en-US', {
    weekday: 'long', year: 'numeric', month: 'long', day: 'numeric'
  });

  const containerVariants = {
    hidden: { opacity: 0 },
    show: {
      opacity: 1,
      transition: { staggerChildren: 0.1 }
    }
  };

  const itemVariants = {
    hidden: { opacity: 0, y: 20 },
    show: { opacity: 1, y: 0, transition: { type: "spring", stiffness: 300, damping: 24 } }
  };

  return (
    <Layout>
      <div className="mb-6">
        <Link href="/history" className="inline-flex items-center text-sm font-medium text-muted-foreground hover:text-primary transition-colors">
          <ChevronLeft className="w-4 h-4 mr-1" />
          Back to History
        </Link>
      </div>

      <motion.div 
        className="grid grid-cols-1 lg:grid-cols-5 gap-8 lg:gap-12"
        variants={containerVariants}
        initial="hidden"
        animate="show"
      >
        {/* Left Column: Image */}
        <motion.div variants={itemVariants} className="lg:col-span-2 flex flex-col gap-4">
          <div className="relative rounded-3xl overflow-hidden shadow-lg border border-border/50 bg-black aspect-square lg:aspect-auto lg:h-[500px]">
            <img 
              src={scan.imageUrl} 
              alt="Skin scan" 
              className="w-full h-full object-cover"
            />
            <div className="absolute inset-0 ring-1 ring-inset ring-black/10 rounded-3xl pointer-events-none" />
          </div>
          
          <div className="flex items-center justify-center gap-2 text-sm text-muted-foreground bg-white px-4 py-3 rounded-2xl shadow-sm border border-border/50">
            <Calendar className="w-4 h-4" />
            <span>Analyzed on {formattedDate}</span>
          </div>
        </motion.div>

        {/* Right Column: Results */}
        <div className="lg:col-span-3 space-y-6">
          <motion.div variants={itemVariants}>
            <h1 className="text-3xl font-extrabold tracking-tight mb-2">Analysis Results</h1>
            
            {/* Disclaimer Alert */}
            <div className="flex gap-3 p-4 bg-amber-50 border border-amber-200 text-amber-900 rounded-2xl mb-8">
              <AlertTriangle className="w-5 h-5 text-amber-600 flex-shrink-0 mt-0.5" />
              <p className="text-sm leading-relaxed">
                <strong className="font-semibold block mb-1">Medical Disclaimer</strong>
                This analysis is generated by Artificial Intelligence and is for informational purposes only. It is <strong>not</strong> a medical diagnosis. Always consult a certified dermatologist or healthcare provider for medical concerns.
              </p>
            </div>
          </motion.div>

          <motion.div variants={itemVariants} className="bg-white rounded-3xl p-6 shadow-md shadow-black/5 border border-border/60 relative overflow-hidden">
            <div className="absolute top-0 left-0 w-1 h-full bg-primary" />
            <div className="flex items-start gap-4">
              <div className="p-3 bg-primary/10 rounded-xl text-primary mt-1">
                <CheckCircle2 className="w-6 h-6" />
              </div>
              <div>
                <h3 className="text-lg font-bold text-foreground mb-2">Primary Prediction</h3>
                <p className="text-muted-foreground text-lg leading-relaxed">
                  {scan.prediction || "No specific condition detected or analysis inconclusive."}
                </p>
              </div>
            </div>
          </motion.div>

          <motion.div variants={itemVariants} className="bg-white rounded-3xl p-6 shadow-md shadow-black/5 border border-border/60">
            <div className="flex items-start gap-4">
              <div className="p-3 bg-blue-50 rounded-xl text-blue-600 mt-1">
                <Info className="w-6 h-6" />
              </div>
              <div>
                <h3 className="text-lg font-bold text-foreground mb-2">Health Information</h3>
                <p className="text-muted-foreground leading-relaxed">
                  {scan.healthInfo || "General skin health information not available for this scan."}
                </p>
              </div>
            </div>
          </motion.div>

          <motion.div variants={itemVariants} className="bg-white rounded-3xl p-6 shadow-md shadow-black/5 border border-border/60">
            <div className="flex items-start gap-4">
              <div className="p-3 bg-teal-50 rounded-xl text-teal-600 mt-1">
                <StethoscopeIcon className="w-6 h-6" />
              </div>
              <div>
                <h3 className="text-lg font-bold text-foreground mb-2">Recommendations</h3>
                <p className="text-muted-foreground leading-relaxed whitespace-pre-wrap">
                  {scan.recommendation || "We recommend consulting a professional if you have persistent concerns."}
                </p>
              </div>
            </div>
          </motion.div>

        </div>
      </motion.div>
    </Layout>
  );
}

function StethoscopeIcon(props: React.SVGProps<SVGSVGElement>) {
  return (
    <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" {...props}>
      <path d="M4.8 2.3A.3.3 0 1 0 5 2H4a2 2 0 0 0-2 2v5a6 6 0 0 0 6 6v0a6 6 0 0 0 6-6V4a2 2 0 0 0-2-2h-1a.2.2 0 1 0 .3.3" />
      <path d="M8 15v1a6 6 0 0 0 6 6v0a6 6 0 0 0 6-6v-4" />
      <circle cx="20" cy="10" r="2" />
    </svg>
  );
}
